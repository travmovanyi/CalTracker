def find_food(query: str):
    """Пошук продукту у базі за частковим збігом назви."""
    query = query.lower().strip()
    if query in FOOD_DB:
        return query, FOOD_DB[query]
    matches = [name for name in FOOD_DB if query in name]
    if matches:
        best = min(matches, key=len)
        return best, FOOD_DB[best]
    return None, None
